#include  <unistd.h>

char	*ft_strlowcase(char *str)
{
	char *it;

	it = str;
	while (*it != '\0')
	{
		if (*it >= 'A' && *it <= 'Z')
		{
			*it = *it + 32;
		}
		it++;
	}
	return (str);
}
