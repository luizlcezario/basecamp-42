#include  <unistd.h>

char	*ft_strupcase(char *str)
{
	char *it;

	it = str;
	if(*it != 0)
	{
		while (*it != '\0')
		{
			if (*it >= 'a' && *it <= 'z')
			{
				*it = *it - 32;
			}
			it++;
		}
	}
	return (str);
}
