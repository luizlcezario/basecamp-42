#include  <unistd.h>

char	*ft_strcapitalize(char *str)
{
	char *it;

	it = str;
	while (*it != '\0')
	{
		if (!((*(it - 1) >= 'A' && *(it - 1) <= 'Z')
				|| (*(it - 1) >= 'a' && *(it - 1) <= 'z')
				|| (*(it - 1) >= '0' && *(it - 1) <= '9')))
		{
			if (*it >= 'a' && *it <= 'z')
			{
				*it = *it - 32;
			}
		}
		else if ((*it >= 'A' && *it <= 'Z')
			|| (*it >= 'a' && *it <= 'z') || (*it >= '0' && *it <= '9'))
		{
			if (*it >= 'A' && *it <= 'Z')
			{
				*it = *it + 32;
			}
		}
		it++;
	}
	return (str);
}
