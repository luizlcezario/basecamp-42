int	ft_sqrt(int nb)
{
	int	a;
	int	res;

	a = 0;
	res = 0;
	if (nb <= 0)
		return (0);
	while (res < nb && a <= 46341)
	{
		a++;
		res = a * a;
	}
	if (res == nb)
		return (a);
	return (0);
}
