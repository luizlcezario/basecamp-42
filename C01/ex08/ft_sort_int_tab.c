#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	a;
	int	i;
	int	tmp;

	a = 0;
	while (a < size - 1)
	{
		i = 0;
		while (i < size - a - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				tmp = tab[i + 1];
				tab[i + 1] = tab[i];
				tab[i] = tmp;
			}
			i++;
		}
		a++;
	}
}
